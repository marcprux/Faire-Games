        val rememberedbrowserPresented by rememberSaveable(stateSaver = context.stateSaver as Saver<skip.ui.State<Boolean>, Any>) { mutableStateOf(_browserPresented) }
        _browserPresented = rememberedbrowserPresented

        val rememberedbrowserURL by rememberSaveable(stateSaver = context.stateSaver as Saver<skip.ui.State<URL>, Any>) { mutableStateOf(_browserURL) }
        _browserURL = rememberedbrowserURL

        return super.Evaluate(context, options)
    }

    private fun licensePageURL(): URL? {
        SPDXLicense.licensePageURL(for_ = package_.licenseDeclared)?.let { url ->
            return url.sref()
        }
        return SPDXLicense.licensePageURL(for_ = package_.licenseConcluded)
    }

    /// Looks up an extracted license info entry if the package's `licenseDeclared`
    /// or `licenseConcluded` is a `LicenseRef-…` identifier.
    private fun extractedLicenseInfo(): SBOMExtractedLicense? {
        val candidates = arrayOf(package_.licenseDeclared, package_.licenseConcluded)
        for (candidate in candidates.sref()) {
            val id_0 = candidate
            if ((id_0 == null) || !id_0.hasPrefix("LicenseRef-")) {
                continue
            }
            document.extractedLicense(forId = id_0)?.let { info ->
                return info.sref()
            }
        }
        return null
    }

    /// If the given license id is a `LicenseRef-…`, return its human-readable name when available.
    private fun resolveLicenseName(license: String): String {
        if (license.hasPrefix("LicenseRef-")) {
            document.extractedLicense(forId = license)?.let { info ->
                info.name?.let { name ->
                    if (!name.isEmpty) {
                        return "${name} (${license})"
                    }
                }
            }
        }
        return license
    }

    private constructor(package_: SBOMPackage, document: SBOMDocument, browserPresented: Boolean = false, browserURL: URL = URL(string = "https://spdx.org/licenses/"), privatep: Nothing? = null) {
        this.package_ = package_.sref()
        this.document = document.sref()
        this._browserPresented = skip.ui.State(browserPresented)
        this._browserURL = skip.ui.State(browserURL.sref())
    }

    constructor(package_: SBOMPackage, document: SBOMDocument): this(package_ = package_, document = document, privatep = null) {
    }
}

