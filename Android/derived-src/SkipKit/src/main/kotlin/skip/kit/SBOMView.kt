/// A "Software Bill of Materials" view that displays the third-party software dependencies
/// of the host app, parsed from SPDX SBOM resource files in the given bundle.
///
/// The view looks for SBOM resources generated by `skip sbom create`:
///
/// - On Apple platforms: `sbom-darwin-ios.spdx.json`
/// - On Android: `sbom-linux-android.spdx.json`
///
/// Each dependency is presented as a row showing its name, supplier, version, and declared
/// license. Tapping a row navigates to a detail view that shows all SPDX fields for the
/// package, including checksums and external references. Where the declared license is a
/// recognised SPDX identifier (e.g., `EUPL-1.2`, `Apache-2.0`), the detail view exposes a
/// button that opens the corresponding page on https://spdx.org/licenses/ in an in-app
/// browser, so users can review the full license text.
///
/// The list also includes a button that brings up the system share sheet so users can
/// export the raw SPDX JSON for offline review or auditing.
///
/// `SBOMView` is intended to be dropped into a Settings or Preferences view via a
/// `NavigationLink`:
///
/// ```swift
/// NavigationLink("Bill of Materials") {
///     SBOMView(bundle: Bundle.module)
/// }
/// ```
///
/// This view exists in part to comply with open-source license obligations that require
/// distributing apps to mention the licenses of included software.
@androidx.annotation.Keep
class SBOMView: View, skip.lib.SwiftProjecting {
    internal val bundle: Bundle
    internal val displayMode: SBOMDisplayMode
    private var document: SBOMDocument?
        get() = _document.wrappedValue.sref({ this.document = it })
        set(newValue) {
            _document.wrappedValue = newValue.sref()
        }
    private var _document: skip.ui.State<SBOMDocument?> = skip.ui.State(null)
    private var loadError: String?
        get() = _loadError.wrappedValue
        set(newValue) {
            _loadError.wrappedValue = newValue
        }
    private var _loadError: skip.ui.State<String?> = skip.ui.State(null)

    constructor(bundle: Bundle, displayMode: SBOMDisplayMode = SBOMDisplayMode.hierarchy) {
        this.bundle = bundle
        this.displayMode = displayMode
    }

    override fun body(): View {
        return ComposeBuilder { composectx: ComposeContext ->
            contentView()
                .navigationTitle(LocalizedStringKey(stringLiteral = "Bill of Materials"))
                .task { -> MainActor.run { loadDocument() } }.Compose(composectx)
        }
    }

    @Composable
    @Suppress("UNCHECKED_CAST")
    override fun Evaluate(context: ComposeContext, options: Int): kotlin.collections.List<Renderable> {
        val remembereddocument by rememberSaveable(stateSaver = context.stateSaver as Saver<skip.ui.State<SBOMDocument?>, Any>) { mutableStateOf(_document) }
        _document = remembereddocument

        val rememberedloadError by rememberSaveable(stateSaver = context.stateSaver as Saver<skip.ui.State<String?>, Any>) { mutableStateOf(_loadError) }
        _loadError = rememberedloadError

        return super.Evaluate(context, options)
    }

    private fun contentView(): View {
        return ComposeBuilder { composectx: ComposeContext ->
            linvokeComposable l@{
                val matchtarget_0 = document
                if (matchtarget_0 != null) {
                    val doc = matchtarget_0
                    return@l SBOMListView(document = doc, bundle = bundle, displayMode = displayMode).Compose(composectx)
                } else {
                    val matchtarget_1 = loadError
                    if (matchtarget_1 != null) {
                        val error = matchtarget_1
                        return@l SBOMEmptyView(message = error).Compose(composectx)
                    } else {
                        return@l SBOMEmptyView(message = "No software bill of materials is available for this build.").Compose(composectx)
                    }
                }
            }
            ComposeResult.ok
        }
    }

    private suspend fun loadDocument(): Unit = Async.run {
        try {
            val doc = SBOMDocument.load(from = bundle)
            this.document = doc
        } catch (error: Throwable) {
            @Suppress("NAME_SHADOWING") val error = error.aserror()
            this.loadError = "Failed to parse software bill of materials: ${error.localizedDescription}"
        }
    }

    override fun Swift_projection(options: Int): () -> Any = Swift_projectionImpl(options)
    private external fun Swift_projectionImpl(options: Int): () -> Any

    @androidx.annotation.Keep
    companion object {

        /// Returns `true` if the given bundle contains an SBOM resource for the current platform.
        fun bundleContainsSBOM(bundle: Bundle): Boolean = SBOMDocument.resourceURL(in_ = bundle) != null
    }
}

