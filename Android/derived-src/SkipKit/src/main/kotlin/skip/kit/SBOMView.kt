        this.message = message
    }
}

// MARK: - List

internal class SBOMListView: View {
    internal val document: SBOMDocument
    internal val bundle: Bundle
    private var displayMode: SBOMDisplayMode
        get() = _displayMode.wrappedValue
        set(newValue) {
            _displayMode.wrappedValue = newValue
        }
    private var _displayMode: skip.ui.State<SBOMDisplayMode>

    internal constructor(document: SBOMDocument, bundle: Bundle, displayMode: SBOMDisplayMode) {
        this.document = document.sref()
        this.bundle = bundle
        this._displayMode = State(initialValue = displayMode)
    }

    /// The packages shown at the top of the list, depending on the current display mode.
    private val listedPackages: Array<SBOMPackage>
        get() {
            when (displayMode) {
                SBOMDisplayMode.hierarchy -> return document.topLevelPackages
                SBOMDisplayMode.flat -> return document.dependencyPackages
            }
        }

    override fun body(): View {
        return ComposeBuilder { composectx: ComposeContext ->
            List { ->
                ComposeBuilder { composectx: ComposeContext ->
                    // we do not show the picker on iOS because the was SBOMs are generated are always flattened (because it is derived from the Package.resolved, which does not contain any information about which packages depend on which other packages)
                    Section { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            Picker(LocalizedStringKey(stringLiteral = "Display Mode"), selection = Binding({ _displayMode.wrappedValue }, { it -> _displayMode.wrappedValue = it })) { ->
                                ComposeBuilder { composectx: ComposeContext ->
                                    Text(LocalizedStringKey(stringLiteral = "Flattened")).tag(SBOMDisplayMode.flat).Compose(composectx)
                                    Text(LocalizedStringKey(stringLiteral = "Hierarchical")).tag(SBOMDisplayMode.hierarchy).Compose(composectx)
                                    ComposeResult.ok
                                }
                            }
                            .pickerStyle(PickerStyle.segmented).Compose(composectx)
                            ComposeResult.ok
                        }
                    }.Compose(composectx)

                    Section(content = { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            ForEach(listedPackages) { pkg ->
                                ComposeBuilder { composectx: ComposeContext ->
                                    NavigationLink(destination = SBOMPackageDetailView(package_ = pkg, document = document)) { ->
                                        ComposeBuilder { composectx: ComposeContext ->
                                            SBOMPackageRow(package_ = pkg).Compose(composectx)
                                            ComposeResult.ok
                                        }
                                    }.Compose(composectx)
                                    ComposeResult.ok
                                }
                            }.Compose(composectx)
                            ComposeResult.ok
                        }
                    }, header = { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            Text({
                                val str = LocalizedStringKey.StringInterpolation(literalCapacity = 0, interpolationCount = 0)
                                str.appendLiteral("Dependencies (")
                                str.appendInterpolation(listedPackages.count)
                                str.appendLiteral(")")
                                LocalizedStringKey(stringInterpolation = str)
                            }()).Compose(composectx)
                            ComposeResult.ok
                        }
                    }, footer = { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            Text(footerText).Compose(composectx)
                            ComposeResult.ok
                        }
                    }).Compose(composectx)

                    Section(content = { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            document.creationInfo?.created?.let { created ->
                                if (!created.isEmpty) {
                                    SBOMFieldRow(label = LocalizedStringKey(stringLiteral = "Created"), value = created).Compose(composectx)
                                }
                            }
                            document.creationInfo?.creators.sref()?.let { creators ->
                                if (!creators.isEmpty) {
                                    SBOMFieldRow(label = LocalizedStringKey(stringLiteral = "Generator"), value = creators.joined(separator = ", ")).Compose(composectx)
                                }
                            }
                            document.spdxVersion?.let { version ->
                                if (!version.isEmpty) {
                                    SBOMFieldRow(label = LocalizedStringKey(stringLiteral = "SPDX Version"), value = version).Compose(composectx)
                                }
                            }
                            document.dataLicense?.let { dataLicense ->
                                if (!dataLicense.isEmpty) {
                                    SBOMFieldRow(label = LocalizedStringKey(stringLiteral = "Data License"), value = dataLicense).Compose(composectx)
                                }
                            }

                            SBOMShareButton(document = document, bundle = bundle).Compose(composectx)
                            ComposeResult.ok
                        }
                    }, header = { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            Text(LocalizedStringKey(stringLiteral = "About this Bill of Materials")).Compose(composectx)
                            ComposeResult.ok
                        }
                    }).Compose(composectx)
                    ComposeResult.ok
                }
            }.Compose(composectx)
        }
    }

    @Composable
    @Suppress("UNCHECKED_CAST")
    override fun Evaluate(context: ComposeContext, options: Int): kotlin.collections.List<Renderable> {
        val remembereddisplayMode by rememberSaveable(stateSaver = context.stateSaver as Saver<skip.ui.State<SBOMDisplayMode>, Any>) { mutableStateOf(_displayMode) }
        _displayMode = remembereddisplayMode

        return super.Evaluate(context, options)
    }

    private val footerText: LocalizedStringKey
        get() {
            when (displayMode) {
                SBOMDisplayMode.hierarchy -> return LocalizedStringKey(stringLiteral = "This app includes the open-source software listed above. Tap a dependency to view its license, version information, and any further dependencies it brings in.")
                SBOMDisplayMode.flat -> return LocalizedStringKey(stringLiteral = "This app includes the open-source software listed above. Tap a dependency to view its license and version information.")
            }
        }
}

internal class SBOMPackageRow: View {
    internal val package_: SBOMPackage

    override fun body(): View {
        return ComposeBuilder { composectx: ComposeContext ->
            VStack(alignment = HorizontalAlignment.leading, spacing = 4.0) { ->
                ComposeBuilder { composectx: ComposeContext ->
                    HStack(alignment = VerticalAlignment.firstTextBaseline) { ->
                        ComposeBuilder { composectx: ComposeContext ->
                            Text(package_.name ?: "Unnamed package")
                                .font(Font.headline)
                                .lineLimit(1).Compose(composectx)
                            Spacer().Compose(composectx)
                            package_.versionInfo?.let { version ->
                                if (!SPDXLicense.isUnknown(version)) {
                                    Text(version)
                                        .font(Font.caption)
                                        .foregroundStyle(Color.secondary)
                                        .lineLimit(1).Compose(composectx)
                                }
                            }
                            ComposeResult.ok
                        }
                    }.Compose(composectx)
                    package_.supplier?.let { supplier ->
                        if (!SPDXLicense.isUnknown(supplier)) {
                            Text(prettySupplier(supplier))
                                .font(Font.caption)
                                .foregroundStyle(Color.secondary)
                                .lineLimit(1).Compose(composectx)
                        }
                    }
                    package_.licenseDeclared?.let { license ->
                        if (!SPDXLicense.isUnknown(license)) {
                            Text(license)
                                .font(Font.caption2)
                                .padding(Edge.Set.horizontal, 6.0)
                                .padding(Edge.Set.vertical, 2.0)
                                .background(Color.secondary.opacity(0.15))
                                .clipShape(Capsule()).Compose(composectx)
                        }
                    }
                    ComposeResult.ok
                }
            }
            .padding(Edge.Set.vertical, 2.0).Compose(composectx)
        }
    }

    /// Strips the SPDX `Organization:` / `Person:` prefix from a supplier string.
    private fun prettySupplier(supplier: String): String {
        if (supplier.hasPrefix("Organization: ")) {
            return String(supplier.dropFirst("Organization: ".count))
        }
        if (supplier.hasPrefix("Person: ")) {
            return String(supplier.dropFirst("Person: ".count))
        }
        return supplier
    }

    constructor(package_: SBOMPackage) {
        this.package_ = package_.sref()
    }
}

