// MARK: - ForEach helper items
//
// SwiftUI's `List` becomes a Compose `LazyColumn` on Android. Multiple `ForEach`
// blocks inside the same `List` share a single key namespace, so using `\.offset`
// or array indices in more than one `ForEach` causes a Compose
// `IllegalArgumentException: Key "0" was already used` crash when the list is
// scrolled. These small `Identifiable` wrappers give each row a globally unique
// string id within the enclosing list.

internal class SBOMSeeAlsoItem: Identifiable<String> {
    override val id: String
    internal val url: String

    constructor(id: String, url: String) {
        this.id = id
        this.url = url
    }

    override fun equals(other: Any?): Boolean {
        if (other !is SBOMSeeAlsoItem) return false
        return id == other.id && url == other.url
    }

    override fun hashCode(): Int {
        var result = 1
        result = Hasher.combine(result, id)
        result = Hasher.combine(result, url)
        return result
    }
}

internal class SBOMIndexedItem: Identifiable<String> {
    override val id: String
    internal val index: Int

    constructor(id: String, index: Int) {
        this.id = id
        this.index = index
    }

    override fun equals(other: Any?): Boolean {
        if (other !is SBOMIndexedItem) return false
        return id == other.id && index == other.index
    }

    override fun hashCode(): Int {
        var result = 1
        result = Hasher.combine(result, id)
        result = Hasher.combine(result, index)
        return result
    }
}

