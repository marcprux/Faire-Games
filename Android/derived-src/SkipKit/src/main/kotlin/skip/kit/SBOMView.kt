// MARK: - Share

internal class SBOMShareButton: View {
    internal val document: SBOMDocument
    internal val bundle: Bundle

    override fun body(): View {
        return ComposeBuilder { composectx: ComposeContext ->
            sbomText()?.let { text ->
                ShareLink(item = text, subject = Text(LocalizedStringKey(stringLiteral = "Software Bill of Materials")), message = Text(document.name ?: "SBOM")) { ->
                    ComposeBuilder { composectx: ComposeContext ->
                        Label(LocalizedStringKey(stringLiteral = "Share Bill of Materials"), systemImage = "square.and.arrow.up").Compose(composectx)
                        ComposeResult.ok
                    }
                }.Compose(composectx)
            }
            ComposeResult.ok
        }
    }

    /// Returns the raw SPDX JSON text for the bundle's SBOM, if available, falling back to
    /// re-encoding the parsed `document` if the original bytes cannot be read.
    private fun sbomText(): String? {
        SBOMDocument.rawData(from = bundle)?.let { data ->
            String(data = data, encoding = StringEncoding.utf8)?.let { s ->
                return s
            }
        }
        val encoder = JSONEncoder()
        encoder.outputFormatting = JSONEncoder.OutputFormatting.of(JSONEncoder.OutputFormatting.prettyPrinted, JSONEncoder.OutputFormatting.sortedKeys)
        (try { encoder.encode(document) } catch (_: Throwable) { null })?.let { data ->
            String(data = data, encoding = StringEncoding.utf8)?.let { s ->
                return s
            }
        }
        return null
    }

    constructor(document: SBOMDocument, bundle: Bundle) {
        this.document = document.sref()
        this.bundle = bundle
    }
}
